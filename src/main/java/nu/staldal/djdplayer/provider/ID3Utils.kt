    fun decodeGenre(genre: String?): String? {
        if (genre != null && genre.length > 2 && genre[0] == '(' && genre[genre.length - 1] == ')') {
            try {
                return GENRE_NAMES[Integer.parseInt(genre.substring(1, genre.length - 1))]
            } catch (e: NumberFormatException) {
                return genre
            } catch (e: ArrayIndexOutOfBoundsException) {
                return genre
            }
        } else {
            try {
                return GENRE_NAMES[Integer.parseInt(genre)]
            } catch (e: NumberFormatException) {
                return genre
            } catch (e: ArrayIndexOutOfBoundsException) {
                return genre
            }
        }
    }
